                                <p style="color:orange;"> <strong>What are structured trade finance transactions ?
                                    </strong> </p>

                                <p> Structured trade finance transactions are where a secured loan is extended to a
                                    seller of goods to finance the production of those goods. Typically, the transaction
                                    is structured so that the proceeds of sale are applied to repay the loan. </p>
                                <p>
                                    There are a number of different types of structured finance transactions. These
                                    include:</p>
                                <ul style="color: #838383;">
                                    <li>Pre-export and prepayment finance – a loan that is repaid directly from the sale
                                        of
                                        goods.</li>
                                    <li>Warehouse financing – a loan secured on the quantity of goods stored in a
                                        warehouse.</li>
                                    <li>Borrowing base facilities – a working capital loan based on a borrowing base of
                                        assets.</li>
                                    <li>Export credit agency finance – where trade agencies of individual countries lend
                                        support to exports of goods from their country.</li>
                                    <li>Trade receivables securitisation – where assets are sold to a financing vehicle
                                        that
                                        issues debt on a capital market.</li>
                                    <li>Islamic finance transactions – transactions structured to be in line with
                                        Islamic
                                        finance principles.</li>
                                    <li>Receivables financing – forfaiting, invoice discounting and factoring.</li>
                                </ul>
                                
                                <p style="color:orange;"><strong>
                                        What is the Uniform Customs and Practice for Documentary Credits ? </strong> </p>
                                <p>
                                    The Uniform Customs and Practice for Documentary Credits (UCP) is a set of
                                    international standards for international trade finance. The UCP brings together the
                                    different domestics laws covering letters of credit into a single set of rules,
                                    standardised by the International Chamber of Commerce (ICC). The UCP presents a
                                    single uniform set of rules and practice and can significantly speed up the process
                                    of negotiating trade finance documents. </p>
                                <p>The UCP is currently in its sixth edition (UCP 600) and is made applicable to a
                                    letter of credit by an express term in the letter of credit. </p>
                                <p>What are the legal considerations in a trade finance transaction?
                                    The key legal considerations in a trade finance transaction include:</p>
                                <ul style="color: #838383;">
                                    <li>The precise obligations of each finance party.</li>
                                    <li> Legal jurisdiction.</li>
                                    <li>Currency risks.</li>
                                    <li>Insurance.</li>
                                </ul>
                                
                                <p>
                                    The precise obligation of the issuing bank will depend on the financing arrangement
                                    chosen. Letters of credit are the most frequent method of payment in international
                                    trade and they impose an obligation on the issuing bank to pay the seller on
                                    presentation of the relevant documents. In more complex deals, a number of banks may
                                    be required. </p>
                                <p>In many letter of credit transactions there are two banks involved, the issuing bank
                                    in the buyer’s home country and the correspondent bank in the seller’s home country.
                                    These banks will enter into bilateral agreements which determine the role of each
                                    party. Care should be taken to ensure that these contracts are consistent with the
                                    governing law of the letter of credit and other related credits. Determining the
                                    governing law of contracts can be complex where the parties are in different
                                    jurisdictions and have different rules on the conflicts of laws. In the event that
                                    no governing law is specified then the general rule is that the governing law will
                                    be that in which the documents are presented by the seller for payment. </p>
                                <p>Export transactions involve payment across country borders and can introduce
                                    material currency risks, particularly where one party does not operate in the
                                    currency of the other country. A buyer may seek to hedge its foreign exchange rate
                                    risk but the costs can be prohibitive. Alternatively, a lender may offer hedging
                                    services in return for a fee. </p>

                                <p style="color:orange;"><strong>What is a typical type of security ? </strong> </p>
                                <p>
                                    A typical type of security in trade finance will depend on the type of trade finance
                                    transaction involved. In asset-based lending, a receivables financier will often
                                    take security over a company’s debts as security for its loan. </p>
                                <p>In pre-export finance facilities, loans are normally secured by taking security over
                                    the sale and purchase contract. A lender may also take security over banks accounts,
                                    including collection accounts. </p>
                                <p>In borrowing base facilities, security is taken over the assets forming the
                                    borrowing base. These are the goods in storage or transit, and security normally
                                    extends to the right to payment for goods and sale proceeds.</p>


                                <p style="color:orange;"><strong>What role does insurance play in trade
                                        finance ?</strong></p>
                                <p>
                                    Insurance plays an important role in trade finance transactions. It provides an
                                    additional layer of protection for a financier. Insurance can help protect against
                                    the risk of damage or destruction of the goods. </p>
                                <p>Common types of insurance in trade finance include:</p>
                                <ul style="color: #838383;">
                                    <li>Asset protection insurance – insurance covering the risk of damage or
                                        destruction of
                                        goods.</li>
                                    <li>Non-payment insurance – insurance covering the risk of non-payment under a
                                        contract.</li>
                                    <li>Political risk insurance – insurance against the risks of a change of law, civil
                                        war, revolution or expropriation of assets.</li>
                                </ul>


                                <p style="color:orange;"><strong>What is pre-export finance ?</strong> </p>
                                <p>
                                    Pre-export finance (PXF) is where a loan is extended to a borrower that is to be
                                    repaid directly from the cashflow generated through the sale of goods or
                                    commodities. </p>
                                <p>Security is typically taken over the borrower’s rights under the export contract
                                    and
                                    the collection account where amounts under the export contract must be paid.</p>
                                <p>
                                    PXF loans are typically used to meet a borrower’s working capital requirements.
                                </p>

                                <p style="color:orange;"><strong>What are the advantages of pre-export
                                        finance ?</strong></p>
                                <p>
                                    Pre-export loans are a form of secured lending and as such can attract more
                                    favourable terms than unsecured lending. The secured nature of the loan means
                                    that a
                                    lender has to take less risk and may therefore offer more favourable terms.
                                    Secured
                                    loans often have a greater access to lenders than higher-risk, unsecured,
                                    financing,
                                    and so a borrower may be able to choose from a range of lenders. </p>
                                <p>Pre-export finance is heavily documented and lenders may be able to anticipate a
                                    borrower’s needs regarding grace periods, indebtedness or permitted disposals
                                    based
                                    on prior transactions. </p>


                                <p style="color:orange;"><strong>What is export credit agency finance ?</strong></p>
                                <p>
                                    Export credit agency (ECA) finance is where the representatives of a country
                                    lend
                                    money to support the export of capital goods and services from that country.
                                    ECAs
                                    have the financial backing of its home state and the obligations it assumes are
                                    considered sovereign obligations of its home state. Their purpose is to support
                                    the
                                    trade policy of their domestic government.
                                </p>

                                <p style="color:orange;"><strong>What are the benefits of export credit
                                        agencies ?</strong> </p>
                                <p>
                                    Most ECAs have very strong credit ratings and they can help counter a gap in
                                    supply
                                    from the commercial markets. If an ECA’s own capital and reserves are
                                    insufficient
                                    then they may be able to call upon its home state to provide additional support.
                                    ECAs may also be willing to provide credit directly by lending to buyers and at
                                    highly competitive rates.
                                </p>

                                <p style="color:orange;"><strong>What is Avalization ?</strong> </p>
                                <p>
                                    Avalization is defined as the endorsement of a bill of exchange. It is the
                                    involvement of a third party whose role is to guarantee the obligations of the
                                    buyer
                                    as per the contract. By ‘Avalizing’ the document, they are acting as a co-signer
                                    on
                                    the contract, and thus share liability. </p>
                                <p>
                                    When companies are trading overseas, the provision of an AVAL may be often
                                    necessary
                                    to make the instrument eligible for discount. </p>

                                <p>One example of a document that utilizes the Avalization process is Promissory
                                    notes.
                                    Technically, a promissory note is a signed document that contains a promise of
                                    payment on a specified date. Promissory notes are used to source methods of
                                    finance
                                    that do not include a banking institution. Although this is generally true,
                                    Banks do
                                    also issue them. This third party can be an individual or a company, the only
                                    requirement is they are willing to bear the liability. </p>

                                <p>Since anyone can issue the Promissory Note, Avalization instils a lot of
                                    confidence
                                    in the transaction. </p>

                                <p style="color:orange;"><strong>Factoring vs. Reverse Factoring </strong></p>
                                <p>Standard invoice factoring allows a business to unlock cash that is tied up in
                                    future income. It includes a company selling its accounts receivables to a third
                                    party – the factor – at a discount. This allows the business access to money
                                    that
                                    would have not otherwise been accessible in the short-term. </p>

                                <p>Reverse factoring, however, is the process of a third party putting itself
                                    between
                                    the buyer and the seller. The third party commits to paying the company’s
                                    invoices
                                    to the suppliers at an accelerated rate in exchange for a discount. </p>

                                <p>The buyer will pay the provider at the agreed invoice due date, and the supplier
                                    receives a much-discounted payment from the Factor.</p>



